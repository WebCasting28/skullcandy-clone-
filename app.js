//first  sub-menu js

let button1 = document.getElementById("btn1");

let box1 = document.getElementById("sub-menu1");

let show1 = document.getElementById("sub-show1");

button1.addEventListener('mouseenter', ()=>{

    
   box1.style.top="60px";
    
})


button1.addEventListener('mouseleave', ()=>{
    box1.style.top="-100%";


}) 
show1.addEventListener('mouseenter', ()=>{
    box1.style.top="60px";


})
show1.addEventListener('mouseleave', ()=>{
    box1.style.top="-100%";


})

